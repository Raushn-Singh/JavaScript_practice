let a = parseInt(prompt("Enter the First Number"))
let b = parseInt(prompt("Enter the second Number"))
let c = parseInt(prompt("Enter the third Number"))

let sum = a + b + c;
let sub = a - b - c;
let mul = a * b * c;
let mod = a % b % c;

console.log(`the sum of three number is : ${sum}  `)
console.log(`the subtraction of three number is : ${sub}  `)
console.log(`the multiplication of three number is : ${mul}  `)
console.log(`the modulus of three number is : ${mod}  `)

